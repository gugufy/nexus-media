    const disk = require( "node:fs" );
    const path = require( "node:path" );
    const { app, BrowserWindow } = require( "electron" );
// ----------------------------------------------------------------------------------------------------------------------------




// define :: createMainWindow : stuff
// ----------------------------------------------------------------------------------------------------------------------------
    function createMainWindow ()
    {
        const mainWindow = new BrowserWindow // object
        ({
            title: "nexus-media",
            // width: 1000,
            // height: 600,
            autoHideMenuBar: true
        });

        mainWindow.loadURL( "file://" + path.join(__dirname,"./window/index.html") );
    }
// ----------------------------------------------------------------------------------------------------------------------------




// listen :: whenReady : invoke new window
// ----------------------------------------------------------------------------------------------------------------------------
    app.whenReady().then(()=>
    {
        createMainWindow();
    })
